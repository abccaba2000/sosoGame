print("Area ofa trapezoid")
height = float(input("Enter the height of the trapezoid : "))
len_bottom = float(input("Enter the length of the bottom base : "))
len_top = float(input("Enter the length of the top base : "))
area_of_trapezoid = (1/2) * (len_bottom + len_top) * height
print("The area is : ", area_of_trapezoid)
